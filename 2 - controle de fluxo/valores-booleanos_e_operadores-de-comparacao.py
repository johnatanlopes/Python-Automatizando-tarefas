# Booleanos
'''
O tipo de dado booleano (Boolean) tem apenas dois valores: True ou False
'''

# Operadores de comparação
'''
== igual a
!= diferente de 
< menor que
> maior que
<= menor ou igual a
>= maior ou igual a
'''

print(42 == 42)
print(42 == 99)
print(2 != 3)
print(2 != 2)
print('hello' == 'hello')
print('hello' == 'Hello')
print('dog' != 'cat')
print(True == True)
print(True != False)
print(42 == 42.0)
print(42 == '42')

print(42 < 100)
print(42 > 100)
print(42 < 42)

eggCount = 42
print(eggCount <= 42)

myAge = 29
print(myAge >= 10)