'''
O método values() retorna os valores que estão armazenados em cada indice
'''
spam = {'color': 'red', 'age': 42}
for v in spam.values():
    print(v)

'''
O método keys retorna os indices
'''
for k in spam.keys():
    print(k)

'''
O método items retorna tuplas contendo o indice e o valor
'''
for i in spam.items():
    print(i)




